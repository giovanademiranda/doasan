class User {
  final String name;
  final String email;
  final String phone;
  final String address;
  final String bloodType;
  final String birthDate;
  final String weight;

  User({
    required this.name,
    required this.email,
    required this.phone,
    required this.address,
    required this.bloodType,
    required this.birthDate,
    required this.weight,
  });
}
